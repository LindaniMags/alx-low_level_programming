#include "3-calc.h"

/**
 * op_add - sum of a, b
 * @a: integer a
 * @b: integer b
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference of a, b
 * @a: integer a
 * @b: integer b
 *
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of a, b
 * @a: integer a
 * @b: integer b
 *
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of a, b
 * @a: integer a
 * @b: integer b
 *
 * Return: quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo of a, b
 * @a: integer a
 * @b: integer b
 *
 * Return: modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
